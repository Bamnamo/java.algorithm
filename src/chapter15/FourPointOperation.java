package chapter15;

public class FourPointOperation {
    public static void main(String[]args) {
        int a = 7;
        int b = 3;

        int add = a + b;
        int sub = a - b;
        int mult = a * b;
        int div = a / b;
        int mod = a % b;

        System.out.println(add);
        System.out.println(sub);
        System.out.println(mult);
        System.out.println(div);
        System.out.println(mod);
    }
}
